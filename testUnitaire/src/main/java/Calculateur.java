public class Calculateur {
    public static int additionner(int a, int b){
        return a+b;
    }

    public static int soustraire(int a, int b){
        return a-b;
    }

    public static int multiplier(int a, int b){
        return a*b;
    }

    public static int diviser(int a, int b){
        return a/b;
    }
}
